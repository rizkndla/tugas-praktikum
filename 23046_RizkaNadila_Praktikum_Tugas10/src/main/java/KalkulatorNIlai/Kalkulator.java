    private void simpanBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanBtnActionPerformed
        double n1 = Double.parseDouble(N1Tx.getText());
        double n2 = Double.parseDouble(N2Tx.getText());
        double n3 = Double.parseDouble(N3Tx.getText());
        double n4 = Double.parseDouble(N4Tx.getText());
        double n5 = Double.parseDouble(N5Tx.getText());
        datamhs.insertData(NIMTx.getText(), NamaTx.getText(), AlamatTx.getText(),
                MatkulTx.getText(), n1, n2, n3, n4, n5);
        ClearTextField();
        viewDataTable();

        NATx.setText(String.valueOf((n1 * 10/100) + (n2 * 15/100) + (n3 * 25/100) + (n4 * 15/100) + (n5 * 35/100)));


    }//GEN-LAST:event_simpanBtnActionPerformed

    private void hapusBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusBtnActionPerformed
        datamhs.deleteData(mahasiswaTB.getSelectedRow());
        NATx.setText("");
        viewDataTable();
    }//GEN-LAST:event_hapusBtnActionPerformed

