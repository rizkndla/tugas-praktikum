    public final void viewDataTable() {
        String[] namakolom = {"Nim", "Nama", "Alamat", "Mata kuliah", "Nilai Akhir"};
        Object[][] objekmahasiswa = new Object[datamhs.getALL().size()][5];
        int i = 0;
        for (Mhs mhs : datamhs.getALL()) {
            objekmahasiswa[i] = new Object[]{
                mhs.NIM(),
                mhs.Nama(),
                mhs.Alamat(),
                mhs.Matakuliah(),
                mhs.NilaiAkhir()
            };
            i++;
        }
        modelmhs = new CustomTableModel(objekmahasiswa, namakolom);
        mahasiswaTB.setModel(modelmhs);
    }

